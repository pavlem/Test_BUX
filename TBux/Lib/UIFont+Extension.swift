import UIKit

extension UIFont {
    static var buxBold16: UIFont { return UIFont.boldSystemFont(ofSize: 16) }
    static var buxBold17: UIFont { return UIFont.boldSystemFont(ofSize: 17) }
    static var buxRegular: UIFont { return UIFont.systemFont(ofSize: 17) }

    static var buxBlack15: UIFont { return UIFont.systemFont(ofSize: 15, weight: .black) }
    static var buxBlack22: UIFont { return UIFont.systemFont(ofSize: 22, weight: .black) }
}
